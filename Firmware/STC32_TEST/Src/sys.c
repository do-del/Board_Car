#include "sys.h"

//========================================================================
// 函数: void delay_ms(u8 ms)
// 描述: 延时函数。
// 参数: ms,要延时的ms数, 这里只支持1~255ms. 自动适应主时钟.
// 返回: none.
// 版本: VER1.0
// 日期: 2021-3-9
// 备注: 
//========================================================================
void delay_ms(unsigned short ms)
{
     unsigned short i;
     do{
          i = MAIN_FOSC / 6000;
          while(--i);   //6T per loop
     }while(--ms);
}
